export default function CouponButton({
    className = "",
    disabled,
    children,
    ...props
}) {
    return (
        <button
            {...props}
            className={
                `inline-flex items-center px-4 py-4 border border-gray-400 bg-disabledBackground rounded-md font-semibold text-s text-disabled tracking-wide hover:bg-gray-300 transition ease-in-out duration-150 ${
                    disabled && "opacity-25"
                } ` + className
            }
            disabled={disabled}
        >
            {children}
        </button>
    );
}
